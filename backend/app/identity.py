COLORS = ["#6366f1", "#8b5cf6", "#ec4899", "#14b8a6", "#f59e0b", "#3b82f6", "#f97316", "#22c55e"]


def gen_nickname(emotion: str, avoid: str | None = None) -> str:
    """生成情绪意象昵称；avoid 用于避开同房间已存在的名字。"""
    bases = BASES.get(emotion, ["路过的人", "匿名", "陌生人"])
    mods = MODS.get(emotion, ["此刻的", "夜里的", "安静的"])
    for _ in range(8):
        name = random.choice(mods) + random.choice(bases)
        if name != avoid:
            return name
    return random.choice(mods) + random.choice(bases) + "·2"


def gen_color() -> str:
    return random.choice(COLORS)
